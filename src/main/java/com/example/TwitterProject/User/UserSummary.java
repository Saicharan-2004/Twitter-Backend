package com.example.TwitterProject.User;

public class UserSummary {
    private Long userID;
    private String name;
    // getters and setters

    public Long getUserID() {
        return userID;
    }

    public void setUserID(Long userID) {
        this.userID = userID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}